public class Task2 {
    public static void main(String[] args) {

        // Примитивный тип данных.
        // Хранит само значение непосредственно в памяти
        int number = 10;

        // Ссылочный тип данных.
        // Хранит ссылку на объект в памяти
        String text = "Hello, Java";

        System.out.println("Значение переменной number (int): " + number);
        System.out.println("Значение переменной text (String): " + text);
    }
}